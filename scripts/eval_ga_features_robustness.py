#!/usr/bin/env python3
from __future__ import annotations

import argparse
import json
import math
from pathlib import Path
from typing import Dict, List, Optional, Sequence, Tuple

import numpy as np
import pandas as pd
from sklearn.metrics import roc_auc_score

from ga_learner_feature_synthesis import _build_learner_dataset


def _strip_outer_parens(s: str) -> str:
    s = s.strip()
    if not (s.startswith("(") and s.endswith(")")):
        return s
    depth = 0
    for i, ch in enumerate(s):
        if ch == "(":
            depth += 1
        elif ch == ")":
            depth -= 1
            if depth == 0 and i != len(s) - 1:
                return s
    return s[1:-1].strip()


def _split_top_level_binary(s: str) -> Optional[Tuple[str, str, str]]:
    s = s.strip()
    levels = []
    depth = 0
    for i, ch in enumerate(s):
        if ch == "(":
            depth += 1
        elif ch == ")":
            depth -= 1
        levels.append(depth)

    # low precedence first
    for op in [" max ", " min ", " + ", " - ", " * ", " / "]:
        start = 0
        while True:
            idx = s.find(op, start)
            if idx < 0:
                break
            if idx == 0:
                start = idx + len(op)
                continue
            left_last = idx - 1
            right_first = idx + len(op)
            if right_first >= len(s):
                start = idx + len(op)
                continue
            if levels[left_last] == 0 and levels[right_first] == 0:
                left = s[:idx].strip()
                right = s[idx + len(op) :].strip()
                if left and right:
                    return left, op.strip(), right
            start = idx + len(op)
    return None


def _eval_expr(expr: str, values: Dict[str, np.ndarray]) -> np.ndarray:
    s = expr.strip()
    s = _strip_outer_parens(s)

    if s.startswith("abs(") and s.endswith(")"):
        inside = s[4:-1].strip()
        return np.abs(_eval_expr(inside, values))
    if s.startswith("sqrt_abs(") and s.endswith(")"):
        inside = s[9:-1].strip()
        return np.sqrt(np.abs(_eval_expr(inside, values)) + 1e-12)
    if s.startswith("log1p_abs(") and s.endswith(")"):
        inside = s[10:-1].strip()
        return np.log1p(np.abs(_eval_expr(inside, values)))
    if s.startswith("(-") and s.endswith(")"):
        inside = s[2:-1].strip()
        return -_eval_expr(inside, values)

    split = _split_top_level_binary(s)
    if split is not None:
        left_s, op, right_s = split
        a = _eval_expr(left_s, values)
        b = _eval_expr(right_s, values)
        if op == "+":
            return a + b
        if op == "-":
            return a - b
        if op == "*":
            return a * b
        if op == "/":
            return a / (np.abs(b) + 1e-6)
        if op == "max":
            return np.maximum(a, b)
        if op == "min":
            return np.minimum(a, b)
        raise ValueError(f"Unsupported op: {op}")

    if s in values:
        return values[s]
    try:
        v = float(s)
        n = len(next(iter(values.values())))
        return np.full(n, v, dtype=np.float64)
    except Exception as exc:
        raise KeyError(f"Cannot parse token/feature: {s}") from exc


def _auc_oriented(y: np.ndarray, x: np.ndarray) -> float:
    if len(np.unique(y)) < 2:
        return float("nan")
    if np.allclose(x, x[0]):
        return float("nan")
    auc = float(roc_auc_score(y, x))
    return float(max(auc, 1.0 - auc))


def _discover_run_dirs(root: Path) -> List[Path]:
    runs: List[Path] = []
    for p in sorted(root.iterdir()):
        if not p.is_dir():
            continue
        if (p / "config_snapshot.yaml").exists() and (p / "sample_learner_assignments.csv").exists() and (p / "learner_label_distribution.csv").exists():
            runs.append(p)
    return runs


def main() -> None:
    parser = argparse.ArgumentParser(description="Evaluate GA feature robustness across all run dirs.")
    parser.add_argument("--features-csv", required=True, help="GA features CSV generated by ga_learner_feature_synthesis.py")
    parser.add_argument("--runs-root", default="outputs/runs", help="Root directory containing run folders")
    parser.add_argument("--out-prefix", default="outputs/analysis/ga_feature_robustness")
    parser.add_argument("--top-k", type=int, default=80, help="Evaluate top-k features from features csv")
    parser.add_argument("--min-samples-per-learner", type=int, default=200)
    parser.add_argument("--attack-ratio-threshold", type=float, default=0.5)
    parser.add_argument("--max-numeric-source-cols", type=int, default=96)
    parser.add_argument("--min-learners", type=int, default=10, help="Skip run if usable learners below this")
    args = parser.parse_args()

    features_df = pd.read_csv(Path(args.features_csv))
    if "expression" not in features_df.columns:
        raise ValueError("features csv missing 'expression' column")
    eval_df = features_df.head(int(args.top_k)).copy()
    eval_df["feature_name"] = eval_df["feature_name"].astype(str)
    eval_df["expression"] = eval_df["expression"].astype(str)

    run_dirs = _discover_run_dirs(Path(args.runs_root))
    if not run_dirs:
        raise RuntimeError(f"No runnable dirs under {args.runs_root}")
    print(f"[1/3] discovered run dirs: {len(run_dirs)}", flush=True)

    per_run_rows: List[Dict[str, object]] = []
    skipped_runs: List[Dict[str, object]] = []

    for i, run_dir in enumerate(run_dirs, start=1):
        try:
            learner_df, _ = _build_learner_dataset(
                run_dir=run_dir.resolve(),
                min_samples_per_learner=int(args.min_samples_per_learner),
                attack_ratio_threshold=float(args.attack_ratio_threshold),
                max_numeric_cols=int(args.max_numeric_source_cols),
            )
        except Exception as exc:
            skipped_runs.append({"run_dir": str(run_dir), "reason": f"build_failed: {exc}"})
            continue

        if len(learner_df) < int(args.min_learners):
            skipped_runs.append({"run_dir": str(run_dir), "reason": f"usable_learners<{int(args.min_learners)}"})
            continue

        y = learner_df["is_attack_learner"].astype(np.int32).to_numpy()
        if len(np.unique(y)) < 2:
            skipped_runs.append({"run_dir": str(run_dir), "reason": "single_class_labels"})
            continue

        values = {
            c: learner_df[c].to_numpy(np.float64)
            for c in learner_df.columns
            if c not in {"learner_name", "attack_ratio", "is_attack_learner"}
        }

        ok_count = 0
        for r in eval_df.itertuples(index=False):
            fname = str(r.feature_name)
            expr = str(r.expression)
            try:
                x = _eval_expr(expr, values)
                x = np.asarray(x, dtype=np.float64)
                finite_mask = np.isfinite(x)
                if not np.any(finite_mask):
                    raise ValueError("all non-finite")
                if not np.all(finite_mask):
                    med = float(np.nanmedian(x[finite_mask]))
                    x = np.where(finite_mask, x, med)
                auc_star = _auc_oriented(y, x)
                if not np.isfinite(auc_star):
                    continue
                per_run_rows.append(
                    {
                        "run_dir": str(run_dir),
                        "feature_name": fname,
                        "expression": expr,
                        "auc_oriented": float(auc_star),
                        "learners_used": int(len(learner_df)),
                        "attack_learners": int((y == 1).sum()),
                        "benign_learners": int((y == 0).sum()),
                    }
                )
                ok_count += 1
            except Exception:
                continue
        print(f"      [{i:03d}/{len(run_dirs)}] {run_dir.name}: scored_features={ok_count}", flush=True)

    if not per_run_rows:
        raise RuntimeError("No per-run feature AUC results produced.")

    print("[2/3] aggregating robustness stats ...", flush=True)
    pr = pd.DataFrame(per_run_rows)
    agg_rows: List[Dict[str, object]] = []
    for (fname, expr), g in pr.groupby(["feature_name", "expression"], sort=False):
        aucs = g["auc_oriented"].to_numpy(np.float64)
        agg_rows.append(
            {
                "feature_name": str(fname),
                "expression": str(expr),
                "run_count": int(len(g)),
                "auc_mean": float(np.mean(aucs)),
                "auc_median": float(np.median(aucs)),
                "auc_std": float(np.std(aucs)),
                "auc_min": float(np.min(aucs)),
                "auc_max": float(np.max(aucs)),
                "pass_080_ratio": float(np.mean(aucs >= 0.80)),
                "pass_090_ratio": float(np.mean(aucs >= 0.90)),
                "pass_095_ratio": float(np.mean(aucs >= 0.95)),
            }
        )
    agg = pd.DataFrame(agg_rows).sort_values(
        by=["auc_mean", "auc_min", "pass_090_ratio"],
        ascending=[False, False, False],
    )

    out_prefix = Path(args.out_prefix)
    out_prefix.parent.mkdir(parents=True, exist_ok=True)
    per_run_csv = out_prefix.with_suffix(".per_run_auc.csv")
    robust_csv = out_prefix.with_suffix(".robustness.csv")
    skipped_json = out_prefix.with_suffix(".skipped_runs.json")
    summary_json = out_prefix.with_suffix(".json")
    report_md = out_prefix.with_suffix(".md")

    pr.to_csv(per_run_csv, index=False)
    agg.to_csv(robust_csv, index=False)
    skipped_json.write_text(json.dumps(skipped_runs, ensure_ascii=False, indent=2), encoding="utf-8")

    summary = {
        "features_csv": str(args.features_csv),
        "runs_root": str(args.runs_root),
        "run_dirs_found": int(len(run_dirs)),
        "run_dirs_scored": int(pr["run_dir"].nunique()),
        "run_dirs_skipped": int(len(skipped_runs)),
        "top_k_evaluated": int(args.top_k),
        "best_robust_feature": None if agg.empty else agg.iloc[0].to_dict(),
        "outputs": {
            "per_run_auc_csv": str(per_run_csv),
            "robustness_csv": str(robust_csv),
            "skipped_runs_json": str(skipped_json),
            "report_md": str(report_md),
        },
    }
    summary_json.write_text(json.dumps(summary, ensure_ascii=False, indent=2), encoding="utf-8")

    print("[3/3] writing report ...", flush=True)
    top_show = agg.head(30)
    with report_md.open("w", encoding="utf-8") as f:
        f.write("# GA Feature Robustness Across Runs\n\n")
        f.write(f"- features_csv: `{args.features_csv}`\n")
        f.write(f"- runs_root: `{args.runs_root}`\n")
        f.write(f"- run_dirs_found: {int(len(run_dirs))}\n")
        f.write(f"- run_dirs_scored: {int(pr['run_dir'].nunique())}\n")
        f.write(f"- run_dirs_skipped: {int(len(skipped_runs))}\n")
        f.write(f"- top_k_evaluated: {int(args.top_k)}\n\n")
        f.write("## Top Robust Features\n\n")
        f.write("| Rank | Feature | AUC Mean | AUC Median | AUC Std | AUC Min | Pass@0.90 | Pass@0.95 | Expression |\n")
        f.write("|---:|---|---:|---:|---:|---:|---:|---:|---|\n")
        for i, r in enumerate(top_show.itertuples(index=False), start=1):
            f.write(
                f"| {i} | {r.feature_name} | {float(r.auc_mean):.4f} | {float(r.auc_median):.4f} | "
                f"{float(r.auc_std):.4f} | {float(r.auc_min):.4f} | {float(r.pass_090_ratio):.2%} | "
                f"{float(r.pass_095_ratio):.2%} | `{str(r.expression).replace('`', '')}` |\n"
            )

    print(f"per_run_auc_csv={per_run_csv}", flush=True)
    print(f"robustness_csv={robust_csv}", flush=True)
    print(f"summary_json={summary_json}", flush=True)
    print(f"report_md={report_md}", flush=True)


if __name__ == "__main__":
    main()
